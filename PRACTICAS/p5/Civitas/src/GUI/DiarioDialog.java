    void mostrarEventos(){
        String texto = "";
        while(Diario.getInstance().eventosPendientes()){
            texto += Diario.getInstance().leerEvento() + "\n";
        }
        area.setText(texto);
        this.setVisible(true);
        this.repaint();
        this.revalidate();
    }
    
